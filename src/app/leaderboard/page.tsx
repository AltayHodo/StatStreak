export default function Leaderboard() {
  const supabase = useSupabaseClient();
  const [users, setUsers] = useState<UserRow[]>([]);
  const [loading, setLoading] = useState(true);
  const [sortBy, setSortBy] = useState<'total_score' | 'accuracy'>(
    'total_score'
  );

  useEffect(() => {
    const fetchLeaderboard = async () => {
      setLoading(true);
      const { data, error } = await supabase
        .from('users')
        .select('id, username, accuracy, total_score')
        .order(sortBy, { ascending: false });

      let combinedUsers = [];
      if (!error && data) {
        combinedUsers = [...data, ...SAMPLE_USERS];
        combinedUsers.sort((a, b) => b[sortBy] - a[sortBy]);
        setUsers(combinedUsers);
      }
      setLoading(false);
    };

    fetchLeaderboard();
  }, [supabase, sortBy]);

  useEffect(() => {
    setPage(0);
  }, [users, sortBy]);

  const USERS_PER_PAGE = 50;
  const [page, setPage] = useState(0);

  return (
    <>
      <div className="max-w-2xl mx-auto py-8 px-4">
        <h1 className="text-2xl font-bold mb-6 text-center">Leaderboard</h1>
        <div className="flex justify-end mb-4">
          <label className="mr-2 font-medium">Sort by:</label>
          <select
            value={sortBy}
            onChange={(e) =>
              setSortBy(e.target.value as 'total_score' | 'accuracy')
            }
            className="border rounded px-2 py-1"
          >
            <option value="total_score">Total Score</option>
            <option value="accuracy">Accuracy</option>
          </select>
        </div>
        <table className="w-full bg-white rounded-lg shadow border">
          <thead>
            <tr className="bg-gray-100">
              <th className="py-2 px-4 text-left">Rank</th>
              <th className="py-2 px-4 text-left">Username</th>
              <th className="py-2 px-4 text-right">Total Score</th>
              <th className="py-2 px-4 text-right">Accuracy (%)</th>
            </tr>
          </thead>
          <tbody>
            {loading ? (
              <tr>
                <td colSpan={4} className="text-center py-6">
                  Loading...
                </td>
              </tr>
            ) : users.length === 0 ? (
              <tr>
                <td colSpan={4} className="text-center py-6">
                  No users yet.
                </td>
              </tr>
            ) : (
              users
                .slice(page * USERS_PER_PAGE, (page + 1) * USERS_PER_PAGE)
                .map((user, idx) => (
                  <tr key={user.id} className="border-t">
                    <td className="py-2 px-4 font-semibold text-gray-700">
                      {page * USERS_PER_PAGE + idx + 1}
                    </td>
                    <td className="py-2 px-4">{user.username}</td>
                    <td className="py-2 px-4 text-right">{user.total_score}</td>
                    <td className="py-2 px-4 text-right">
                      {user.accuracy ? user.accuracy.toFixed(2) : '0.00'}
                    </td>
                  </tr>
                ))
            )}
          </tbody>
        </table>
        <div className="flex justify-center mt-4 space-x-4">
          <button
            onClick={() => setPage((p) => Math.max(0, p - 1))}
            disabled={page === 0}
            className="px-4 py-2 bg-gray-200 rounded disabled:opacity-50"
          >
            Previous
          </button>
          <span className="self-center">
            Page {page + 1} of {Math.ceil(users.length / USERS_PER_PAGE)}
          </span>
          <button
            onClick={() =>
              setPage((p) =>
                Math.min(Math.ceil(users.length / USERS_PER_PAGE) - 1, p + 1)
              )
            }
            disabled={page >= Math.ceil(users.length / USERS_PER_PAGE) - 1}
            className="px-4 py-2 bg-gray-200 rounded disabled:opacity-50"
          >
            Next
          </button>
        </div>
      </div>
    </>
  );
}
